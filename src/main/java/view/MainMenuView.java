    /// mengisi tabel dengan data2
    private void setupTable(){
        /// mengambil data terbaru
        viewModel.fetchUserData();
        ArrayList<User> users = viewModel.getData();
        
        /// pengisian nama kolom dan nilai-nilai-nya
        String[] colNames = {"No.", "Name", "Scores", "Standings"};
        Object[][] data = null;
        if(users != null){
            data = new Object[users.size()][colNames.length];
            for(int i = 0; i < users.size(); i++){
                User user = users.get(i);
                data[i][0] = i + 1;
                data[i][1] = user.getName();
                data[i][2] = user.getScore();
                data[i][3] = user.getLandings();
            }
        }
        TableModel m = new DefaultTableModel(data, colNames);
        mainTable.setModel(m);
        
    }

