    private void playBBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playBBtnActionPerformed
        String name = usernameField.getText();
        
        if(name != null){
            
            /// bila nama tidak ada di db, anggap membuat
            /// player baru
            if(viewModel.getDataByName(name) == null){
                viewModel.insertUser(name);
                setupTable();
            }
            
            /// masuk ke "game" dengan nama 
            /// player yang dimasukkan
            game.switchToGameView(name);
        }
    }//GEN-LAST:event_playBBtnActionPerformed

